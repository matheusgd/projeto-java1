/*Operadores Relacionais [Modulo 8.5]
== Igual
!= Diferente
< Menor
> Maior
<= Menor ou Igual
>= Maior ou Igual */
package curso.java.basico;

public class OperadoresRelacionais {

    public static void main(String[] args) {

        int nota1 = 90;
        int nota2 = 90;

        if (nota1 == nota2){
            System.out.println("As Notas são iguais!");
        } else {
            System.out.println("As notas são diferentes!");
        }

    }

}
